namespace Blast\Bundle\GridBundle\Filter\Form;

use Sylius\Component\Grid\Filter\BooleanFilter;
use Symfony\Component\Form\AbstractType;
use Symfony\Component\Form\Extension\Core\Type\ChoiceType;
use Symfony\Component\OptionsResolver\OptionsResolver;

class BooleanFilterType extends AbstractType
{
    /**
     * {@inheritdoc}
     */
    public function configureOptions(OptionsResolver $resolver): void
    {
        $resolver
            ->setDefaults([
                'choices' => [
                    'blast.ui.yes_label' => BooleanFilter::TRUE,
                    'blast.ui.no_label'  => BooleanFilter::FALSE,
                ],
                'required'    => false,
                'placeholder' => 'blast.ui.all_label',
            ])
        ;
    }

    /**
     * {@inheritdoc}
     */
    public function getParent(): string
    {
        return ChoiceType::class;
    }

    /**
     * {@inheritdoc}
     */
    public function getBlockPrefix(): string
    {
        return 'blast_grid_filter_boolean';
    }
}
